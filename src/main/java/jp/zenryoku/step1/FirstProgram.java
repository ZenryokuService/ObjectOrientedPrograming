package steps.java.step1;

/**
 * 初めのプログラム「Hello World」の実装。
 * そして、簡単な絵をかいてみる。
 *
 */
public class FirstProgram {
	/** メインメソッド */
	public static void main(String[] args) {
		System.out.println("Hello World!");
	}

	/**
	 * ハローワールドが動かせたら、「＊(アスタリスク)」を使用して
	 * 簡単な絵をかいてみましょう
	 * 例えば四角を描く
	 */
	public static void firstChallenge() {
		System.out.println("*****");
		System.out.println("*   *");
		System.out.println("*   *");
		System.out.println("*****");
	}

	/**
	 * ハローワールドが動かせたら、「＊(アスタリスク)」を使用して
	 * 簡単な絵をかいてみましょう
	 * 例えば三角を描く
	 */
	public static void firstChallenge2() {
		System.out.println("   *");
		System.out.println("  * *");
		System.out.println(" *   *");
		System.out.println("*******");
	}

	/**
	 * ハローワールドが動かせたら、「＊(アスタリスク)」を使用して
	 * 簡単な絵をかいてみましょう
	 * 例えば逆三角を描く
	 */
	public static void firstChallenge3() {
		System.out.println("*******");
		System.out.println(" *   *");
		System.out.println("  * *");
		System.out.println("   *");
	}

	/**
	 * ハローワールドが動かせたら、「＊(アスタリスク)」を使用して
	 * 簡単な絵をかいてみましょう
	 * 例えばひし形を描く
	 */
	public static void firstChallenge4() {
		System.out.println("   * ");
		System.out.println(" *   *");
		System.out.println("*     *");
		System.out.println(" *   *");
		System.out.println("   *");
	}

	/**
	 * ハローワールドが動かせたら、「＊(アスタリスク)」を使用して
	 * 簡単な絵をかいてみましょう
	 * 例えば大文字焼を描く
	 */
	public static void firstChallenge5() {
		System.out.println("                **");
		System.out.println("              **  **");
		System.out.println("            **      **");
		System.out.println("          **     *     **");
		System.out.println("        **   * * * * *   **");
		System.out.println("      **         *         **");
		System.out.println("    **         *   *         **");
		System.out.println(" **         *         *         **");
		System.out.println("** * ** ** ** * * * ** * ** ** * **");
	}

}
